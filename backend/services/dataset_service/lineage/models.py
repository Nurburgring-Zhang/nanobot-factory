"""P4-4-W2 lineage ORM + Pydantic models.

Inspired by OpenMetadata's Lineage entity, but kept small and self-contained.

Tables (3 SQLAlchemy ORM tables on the lineage engine):
  * lin_edges         — directed edge: from_entity -> to_entity
  * lin_assets        — materialized asset registry (table/column/dataset/pipeline/model)
  * lin_runs          — collect-trigger audit (who/what/when/how-many)

Edge types follow the OpenMetadata + industry vocabulary:
  derived_from  — A is computed from B (CTE / subquery / DataFrame op)
  copied_to     — A was bulk-copied to B
  cleaned_by    — A was transformed by an operator (cleaning)
  scored_by     — A was scored by an operator (scoring)
  trained_by    — A was used as training input for a model
  generated_by  — A was generated by a pipeline / DAG
  refreshed_by   — A is periodically refreshed by a job
  manual        — user-declared link

Entity types:
  table / column / dataset / pipeline / model / job
"""
from __future__ import annotations

import json
import time
import uuid
from typing import Any, Dict, List, Optional

from pydantic import BaseModel, Field
from sqlalchemy import (
    Column as SAColumn,
    Index,
    String,
    Text,
    create_engine,
    event,
)
from sqlalchemy.engine import Engine
from sqlalchemy.orm import DeclarativeBase, Session, sessionmaker


# ── Helpers ─────────────────────────────────────────────────────────────────
def _now() -> str:
    return time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())


def _new_id() -> str:
    return uuid.uuid4().hex


def _loads(s: Optional[str], default):
    if not s:
        return default
    try:
        return json.loads(s)
    except Exception:
        return default


def _dumps(obj: Any) -> str:
    return json.dumps(obj, ensure_ascii=False, separators=(",", ":"))


# ── Edge / asset type vocab (public) ─────────────────────────────────────────
EDGE_TYPES = frozenset(
    {
        "derived_from",
        "copied_to",
        "cleaned_by",
        "scored_by",
        "trained_by",
        "generated_by",
        "refreshed_by",
        "manual",
    }
)
ENTITY_TYPES = frozenset(
    {"table", "column", "dataset", "pipeline", "model", "job"}
)
COLLECT_SOURCES = frozenset(
    {"sql", "ast", "operator", "manual", "scan"}
)


# ── SQLAlchemy base ──────────────────────────────────────────────────────────
class Base(DeclarativeBase):
    """Declarative base for the 3 lineage ORM tables."""


# ── ORM: lin_assets ──────────────────────────────────────────────────────────
class AssetORM(Base):
    """A registered asset in the lineage graph.

    Each asset has a stable ``qualified_name`` like
    ``pg.public.users`` or ``ds.coco_v1`` or ``pipeline.clean_2024``.
    """

    __tablename__ = "lin_assets"

    id = SAColumn(String(32), primary_key=True, default=_new_id)
    qualified_name = SAColumn(String(256), nullable=False, unique=True)
    entity_type = SAColumn(String(32), nullable=False)  # table/column/dataset/pipeline/model
    name = SAColumn(String(256), nullable=False)
    owner = SAColumn(String(128), default="")
    team = SAColumn(String(128), default="")
    tier = SAColumn(String(16), default="bronze")  # bronze/silver/gold
    status = SAColumn(String(16), default="active")  # active/deprecated/draft
    description = SAColumn(Text, default="")
    extra = SAColumn(Text, default="{}")
    created_at = SAColumn(String(32), default=_now, nullable=False)
    updated_at = SAColumn(String(32), default=_now, nullable=False)

    __table_args__ = (
        Index("ix_lin_assets_type", "entity_type"),
        Index("ix_lin_assets_owner", "owner"),
        Index("ix_lin_assets_team", "team"),
    )


# ── ORM: lin_edges ───────────────────────────────────────────────────────────
class EdgeORM(Base):
    """A directed edge: from_asset → to_asset, with provenance metadata."""

    __tablename__ = "lin_edges"

    id = SAColumn(String(32), primary_key=True, default=_new_id)
    from_entity = SAColumn(String(256), nullable=False)  # qualified_name
    from_type = SAColumn(String(32), nullable=False)
    to_entity = SAColumn(String(256), nullable=False)
    to_type = SAColumn(String(32), nullable=False)
    edge_type = SAColumn(String(32), nullable=False)  # see EDGE_TYPES
    pipeline_id = SAColumn(String(128), default="")
    sql = SAColumn(Text, default="")
    script = SAColumn(Text, default="")
    source = SAColumn(String(32), default="manual")  # sql/ast/operator/manual/scan
    columns_json = SAColumn(Text, default="[]")  # [{from_col, to_col}, ...] for column lineage
    extra = SAColumn(Text, default="{}")
    created_at = SAColumn(String(32), default=_now, nullable=False)

    __table_args__ = (
        Index("ix_lin_edges_from", "from_entity"),
        Index("ix_lin_edges_to", "to_entity"),
        Index("ix_lin_edges_pipeline", "pipeline_id"),
        Index(
            "uq_lin_edges",
            "from_entity",
            "to_entity",
            "edge_type",
            "pipeline_id",
            "source",
            unique=True,
        ),
    )


# ── ORM: lin_runs ────────────────────────────────────────────────────────────
class RunORM(Base):
    """Audit trail of a lineage collection run."""

    __tablename__ = "lin_runs"

    id = SAColumn(String(32), primary_key=True, default=_new_id)
    source = SAColumn(String(32), nullable=False)  # sql/ast/operator/manual/scan
    pipeline_id = SAColumn(String(128), default="")
    operator_id = SAColumn(String(128), default="")
    inputs_json = SAColumn(Text, default="[]")
    outputs_json = SAColumn(Text, default="[]")
    edges_added = SAColumn(String(16), default="0")
    ok = SAColumn(String(8), default="true")
    message = SAColumn(Text, default="")
    created_at = SAColumn(String(32), default=_now, nullable=False)

    __table_args__ = (
        Index("ix_lin_runs_source", "source"),
        Index("ix_lin_runs_pipeline", "pipeline_id"),
    )


# ── Pydantic schemas ────────────────────────────────────────────────────────
class _Base(BaseModel):
    model_config = {"from_attributes": True}


class Asset(_Base):
    id: str
    qualified_name: str
    entity_type: str
    name: str
    owner: str = ""
    team: str = ""
    tier: str = "bronze"
    status: str = "active"
    description: str = ""
    extra: Dict[str, Any] = Field(default_factory=dict)
    created_at: str = ""
    updated_at: str = ""


class Edge(_Base):
    id: str
    from_entity: str
    from_type: str
    to_entity: str
    to_type: str
    edge_type: str
    pipeline_id: str = ""
    sql: str = ""
    script: str = ""
    source: str = "manual"
    columns: List[Dict[str, Any]] = Field(default_factory=list)
    extra: Dict[str, Any] = Field(default_factory=dict)
