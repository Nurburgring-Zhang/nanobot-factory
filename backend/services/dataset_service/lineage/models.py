class Run(_Base):
    id: str
    source: str
    pipeline_id: str = ""
    operator_id: str = ""
    inputs: List[str] = Field(default_factory=list)
    outputs: List[str] = Field(default_factory=list)
    edges_added: int = 0
    ok: bool = True
    message: str = ""
    created_at: str = ""


# ── Engine bootstrap (mirrors metadata.models) ──────────────────────────────
_lineage_engine: Optional[Engine] = None
_lineage_session_factory: Optional[sessionmaker] = None


def _build_lineage_engine(url: str) -> Engine:
    if url.startswith("sqlite"):
        eng = create_engine(
            url,
            connect_args={"check_same_thread": False, "timeout": 30},
            pool_pre_ping=True,
        )

        @event.listens_for(eng, "connect")
        def _set_sqlite_pragma(dbapi_conn, _record):  # noqa: ANN001
            try:
                cur = dbapi_conn.cursor()
                cur.execute("PRAGMA foreign_keys=ON")
                cur.execute("PRAGMA journal_mode=WAL")
                cur.close()
            except Exception:
                pass
        return eng
    return create_engine(url, pool_pre_ping=True)


def init_lineage_db(
    db_url: Optional[str] = None,
    *,
    auto_create: bool = True,
) -> Engine:
    """Initialise the lineage engine + (optionally) ``create_all`` tables.

    Resolution order:
      1. ``db_url`` arg
      2. ``IMDF_P2_DB_URL`` env (share with metadata)
      3. ``LINEAGE_DB_URL`` env
      4. SQLite under ``backend/data/lineage.db``

    Safe to call multiple times — only the first call wires the engine.
    """
    global _lineage_engine, _lineage_session_factory

    if _lineage_engine is not None:
        if auto_create:
            try:
                Base.metadata.create_all(bind=_lineage_engine)
            except Exception:
                pass
        return _lineage_engine

    import os
    from pathlib import Path

    if not db_url:
        db_url = (
            os.environ.get("IMDF_P2_DB_URL", "").strip()
            or os.environ.get("LINEAGE_DB_URL", "").strip()
        )
    if not db_url:
        # Default: SQLite under backend/data/lineage.db
        here = Path(__file__).resolve().parents[3]  # backend/
        data_dir = here / "data"
        data_dir.mkdir(parents=True, exist_ok=True)
        db_url = f"sqlite:///{(data_dir / 'lineage.db').as_posix()}"

    _lineage_engine = _build_lineage_engine(db_url)
    _lineage_session_factory = sessionmaker(
        autocommit=False, autoflush=False, bind=_lineage_engine, expire_on_commit=False
    )
    if auto_create:
        Base.metadata.create_all(bind=_lineage_engine)
    return _lineage_engine


def get_lineage_session() -> Session:
    """Yield a lineage Session (caller closes)."""
    if _lineage_session_factory is None:
        init_lineage_db()
    assert _lineage_session_factory is not None
    return _lineage_session_factory()


def reset_lineage_engine() -> None:
    """Test hook: drop the cached engine so tests can re-init with a tmp DB."""
    global _lineage_engine, _lineage_session_factory
    if _lineage_engine is not None:
        try:
            _lineage_engine.dispose()
        except Exception:
            pass
    _lineage_engine = None
    _lineage_session_factory = None


# ── ORM → dict helpers ─────────────────────────────────────────────────────
def asset_to_dict(obj) -> Dict[str, Any]:
    if obj is None:
        return {}
    d: Dict[str, Any] = {c.name: getattr(obj, c.name) for c in obj.__table__.columns}
    d["extra"] = _loads(d.get("extra"), {})
    return d


def edge_to_dict(obj) -> Dict[str, Any]:
    if obj is None:
        return {}
    d: Dict[str, Any] = {c.name: getattr(obj, c.name) for c in obj.__table__.columns}
    d["columns"] = _loads(d.get("columns_json"), [])
    d["extra"] = _loads(d.get("extra"), {})
    d["ok"] = d.get("ok", "true") in ("true", "True", "1", 1)
    return d


def run_to_dict(obj) -> Dict[str, Any]:
    if obj is None:
        return {}
    d: Dict[str, Any] = {c.name: getattr(obj, c.name) for c in obj.__table__.columns}
    d["inputs"] = _loads(d.get("inputs_json"), [])
    d["outputs"] = _loads(d.get("outputs_json"), [])
    d["edges_added"] = int(d.get("edges_added") or 0)
    d["ok"] = d.get("ok", "true") in ("true", "True", "1", 1)
    return d


