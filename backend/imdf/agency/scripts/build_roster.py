def _generate() -> List[dict]:
    out: List[dict] = []
    for department, slug, count, roles in ROSTER:
        if len(roles) != count:
            raise ValueError(
                f"Department {department} count mismatch: expected {count}, defined {len(roles)}"
            )
        for idx, (title_en, title_zh, skills, summary_zh, summary_en, sys_zh, sys_en) in enumerate(roles, start=1):
            expert_id = f"{slug}_expert_{idx:03d}"
            out.append({
                "id": expert_id,
                "name": {"zh": title_zh, "en": title_en},
                "department": department,
                "title": title_en,
                "skills": skills,
                "description": {"zh": summary_zh, "en": summary_en},
                "avatar_url": None,
                "system_prompt": {"zh": sys_zh, "en": sys_en},
            })
    return out


def main() -> None:
    out = _generate()
    target = Path(__file__).resolve().parent.parent / "departments.json"
    target.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(out)} roles to {target}")


if __name__ == "__main__":  # pragma: no cover
    main()
