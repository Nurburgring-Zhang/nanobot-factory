def _insert_before_close(content, block):
    """Insert block before the final closing brace of `export default { ... }`.

    Also ensures a comma is added after the previous `}` if missing, so the
    resulting TS is syntactically valid.
    """
    # Find the export default
    m = re.search(r'export default\s*\{', content)
    if not m:
        return content
    start = m.end()
    depth = 1
    pos = start
    while pos < len(content) and depth > 0:
        ch = content[pos]
        if ch == '{':
            depth += 1
        elif ch == '}':
            depth -= 1
        pos += 1
    # pos is now just after the closing brace
    # Insert block before the closing brace (at pos-1)
    closing_pos = pos - 1
    # Check if previous char (before the final `}`) is `}` from the last
    # namespace — if so, we need a comma between it and our block.
    # Walk back through whitespace to find the last non-whitespace char.
    prev = closing_pos - 1
    while prev >= 0 and content[prev] in ' \t\n\r':
        prev -= 1
    prefix = ''
    if prev >= 0 and content[prev] == '}':
        # Last item closed without a trailing comma — add one.
        prefix = ','
    new_content = content[:closing_pos] + prefix + block + content[closing_pos:]
    return new_content


