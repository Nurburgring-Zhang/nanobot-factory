def main():
    """Parse en-US.ts as plain text and translate it line-by-line, preserving structure."""
    src = os.path.join(LOCALE_DIR, 'en-US.ts')
    with open(src, 'r', encoding='utf-8') as f:
        src_content = f.read()

    # Parse out: namespaces and their keys with their values
    # Each line looks like:
    #   nsName: {
    #     keyName: 'value',
    #     keyName2: 'value',
    #   },
    # Comments are: // ...
    # Multi-line strings don't exist in our files.

    # Strategy: scan for `key: 'value'` patterns, replacing the value
    # Keep the structure intact by operating on line-level.

    # Find all 'key: \'value\'' patterns using regex
    # Captures: key (group 1), value (group 2)
    string_pattern = re.compile(r"(\w+):\s*'([^'\\]*(?:\\.[^'\\]*)*)'", re.MULTILINE)

    # Determine which locale code from filename
    for code in ['ja-JP', 'ko-KR', 'fr-FR', 'de-DE', 'es-ES', 'ru-RU', 'ar-SA']:
        dict_map = DICTS[code]
        translated_count = 0
        unchanged_count = 0
        replaced_count = 0

        def replace_value(m):
            nonlocal replaced_count, unchanged_count
            key = m.group(1)
            value = m.group(2)
            # We use the value (not the key) for translation
            if value in dict_map:
                replaced_count += 1
                new_value = dict_map[value]
                # Escape single quotes in the new value
                new_value_escaped = new_value.replace('\\', '\\\\').replace("'", "\\'")
                return f"{key}: '{new_value_escaped}'"
            unchanged_count += 1
            return m.group(0)

        out_content = string_pattern.sub(replace_value, src_content)

        # Add the new namespaces (10 new) at the end of the object body
        # before the closing brace
        new_ns_block = _build_new_namespaces(code, dict_map)

        # Insert new namespace block before the final closing brace of the
        # `export default { ... }` statement
        out_content = _insert_before_close(out_content, new_ns_block)

        # Update header comment to mention locale
        out_content = re.sub(
            r'/\*\*?\s*\n\s*\*\s*\S.*?\*/',
            f'/**\n * {code} locale messages (auto-generated by P19 v5.2-B).\n * Mirrors structure of en-US.ts; translations via dictionary with English fallback.\n */',
            out_content, count=1, flags=re.DOTALL
        )

        out_path = os.path.join(LOCALE_DIR, f'{code}.ts')
        with open(out_path, 'w', encoding='utf-8') as f:
            f.write(out_content)
        total_leaves = len(string_pattern.findall(src_content))
        print(f"Wrote {out_path}: {total_leaves} leaf keys, {replaced_count} translated, {unchanged_count} kept English")

    print("Done.")


